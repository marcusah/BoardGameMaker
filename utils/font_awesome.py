from kivy.uix.label import Label
from kivy.properties import StringProperty

class FAIcon(Label):
    icon = StringProperty()

    def __init__(self, **kwargs):
        Label.__init__(self, **kwargs)
        self.font_name = 'utils/fontawesome-webfont.ttf'

    def on_icon(self, instance, icon):
        self.text = FONT_AWESOME.get(icon, 'X') if icon.startswith('fa-var') else FONT_AWESOME.get('fa-var-'+icon, 'X')
